package com.intelliTask.core.model.enums;

/**
 * Represents the priority level of a task or project.
 * This enum defines a fixed set of predefined priority levels,
 * ensuring type safety and preventing invalid priority values.
 */
public enum Priority {
    HIGH,    // Indicates a high-priority item
    MEDIUM,  // Indicates a medium-priority item
    LOW,     // Indicates a low-priority item
    NONE     // Indicates no specific priority assigned
}
